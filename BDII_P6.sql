CREATE TABLE DEPORTISTA
(
REVES VARCHAR2(3) NOT NULL,
MANOD VARCHAR2(7) not null,
NACIONALIDAD VARCHAR2(10) NOT NULL,
PUNTOSIN NUMBER(5) DEFAULT 0,
ID_DEPORTISTA VARCHAR2(10),

CONSTRAINT deportista_pk PRIMARY KEY (ID_DEPORTISTA),
CONSTRAINT deportista_fk FOREIGN KEY (ID_DEPORTISTA) REFERENCES PERSONA(ID_PERSONA)
);

CREATE TABLE PARTIDO
(
ID_JUG1 VARCHAR2(10),
ID_JUG2 VARCHAR2(10),
RESULTADO VARCHAR2(3) ,
RONDA NUMBER(6) NOT NULL,
ASISTENCIA NUMBER(3),
NOMBRE VARCHAR2(20) NOT NULL,
NUMERO_PISTA number(6),
CODIGO number(5),
ID_JUEZ varchar2(10),

CONSTRAINT partido_pk PRIMARY KEY (NOMBRE, ID_JUG1, ID_JUG2),
CONSTRAINT partido_fk FOREIGN KEY (ID_JUG1) REFERENCES DEPORTISTA(ID_DEPORTISTA),
CONSTRAINT partido1_fk FOREIGN KEY (ID_JUG2) REFERENCES DEPORTISTA(ID_DEPORTISTA),
CONSTRAINT cmpto_fk FOREIGN KEY (NOMBRE) REFERENCES TORNEO(NOMBRE),
CONSTRAINT pista_partido FOREIGN KEY (CODIGO, NUMERO_PISTA) REFERENCES PISTA(CODIGO, NUMERO_PISTA),
CONSTRAINT JUEZ_PARTIDO    FOREIGN KEY (ID_JUEZ) REFERENCES SILLA(ID_JUEZ)

);

CREATE TABLE SVIP
(
NUM_ASISTENCIA NUMBER(1) DEFAULT 4,
CODIGO_SVIP VARCHAR2(10),

CONSTRAINT svip_pk PRIMARY KEY (CODIGO_SVIP),
CONSTRAINT svip_fk FOREIGN KEY (CODIGO_SVIP) REFERENCES ESPECTADOR(ID_ESPECTADOR),
CONSTRAINT svip1_fk FOREIGN KEY (CODIGO_SVIP) REFERENCES ADMINISTRACION(ID_ADMINISTRACION)

);

CREATE TABLE MARKETING_PROYECTO
(
ID_MARKETING VARCHAR2(10),
ID_PROYECTO VARCHAR2(10),

CONSTRAINT marketing_pro_pk PRIMARY KEY (ID_PROYECTO, ID_MARKETING),
CONSTRAINT marketing_pro1 FOREIGN KEY (ID_MARKETING) REFERENCES MARKETING(ID_MARKETING),
CONSTRAINT marketing_pro2 FOREIGN KEY (ID_PROYECTO) REFERENCES PROYECTO(ID_PROYECTO)

);

CREATE TABLE PATROCINADOR_TORNEO
(
ID_PATROCINADOR VARCHAR(20),
NOMBRE VARCHAR2(20),

CONSTRAINT patrocinador_torneo_pk PRIMARY KEY (ID_PATROCINADOR, NOMBRE),
CONSTRAINT patro_torne1 FOREIGN KEY (ID_PATROCINADOR) REFERENCES PATROCINADOR(ID_PATROCINADOR),
CONSTRAINT patro_torne2 FOREIGN KEY (NOMBRE) REFERENCES TORNEO(NOMBRE)

);

CREATE TABLE PARTIDO_LINEA
(
ID_JUEZ VARCHAR2(10),
ID_JUG1 VARCHAR2(10),
ID_JUG2 VARCHAR2(10),
NOMBRE VARCHAR2(20),

CONSTRAINT partido_linea_pk PRIMARY KEY (NOMBRE, ID_JUG1, ID_JUG2, ID_JUEZ),
CONSTRAINT partido_lineafk FOREIGN KEY (ID_JUEZ) REFERENCES LINEA(ID_JUEZ),
CONSTRAINT partido_lineafk2 FOREIGN KEY (NOMBRE, ID_JUG1, ID_JUG2) REFERENCES PARTIDO(NOMBRE, ID_JUG1, ID_JUG2)

);

CREATE TABLE ESPECTADOR_PARTIDO
(
ID_ESPECTADOR VARCHAR2(10),
ID_JUG1 VARCHAR2(10),
ID_JUG2 VARCHAR2(10),
NOMBRE VARCHAR2(20),

CONSTRAINT espec_partidopk PRIMARY KEY (NOMBRE, ID_JUG1, ID_JUG2, ID_ESPECTADOR),
CONSTRAINT espec_partidofk1 FOREIGN KEY (ID_ESPECTADOR) REFERENCES ESPECTADOR(ID_ESPECTADOR),
CONSTRAINT espec_partidofk2 FOREIGN KEY (NOMBRE, ID_JUG1, ID_JUG2) REFERENCES PARTIDO(NOMBRE, ID_JUG1, ID_JUG2)

);

