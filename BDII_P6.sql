CREATE TABLE TORNEO
(
NOMBRE VARCHAR2(20) NOT NULL,
CIUDAD VARCHAR2(10) NOT NULL,
FECHAFIN DATE NOT NULL,
FECHAINI DATE NOT NULL,
TIPO VARCHAR2(6) NOT NULL,
ID_DIRECTOR VARCHAR2(10) NOT NULL,
