CONSTRAINT dura_pk PRIMARY KEY (CODIGO, NUMERO_PISTA),
CONSTRAINT dura_fk FOREIGN KEY(CODIGO, NUMERO_PISTA) REFERENCES PISTA(CODIGO, NUMERO_PISTA)
