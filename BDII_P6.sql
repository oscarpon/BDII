--Vista actualizabe 1 Vista que muestra los puntos por deportista
CREATE OR REPLACE VIEW DEPORTISTA_NOMBRE(ID_DEPORTISTA, NOMBRE_DEPORTISTA, PUNTOSIN)AS
SELECT d.ID_DEPORTISTA, p.NOMBRE, d.PUNTOSIN FROM DEPORTISTA d, PERSONA p WHERE (d.ID_DEPORTISTA=p.ID_PERSONA);

--Vista no actualizable
CREATE OR REPLACE VIEW PRESU_MARKETING (PRESUPUESTO) AS
   SELECT SUM(SALAR)
   FROM ADMINISTRACION, MARKETING WHERE ID_MARKETING=ID_ADMINISTRACION;

